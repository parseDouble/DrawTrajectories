public class Pointsss {
    private double x;
    private double y;
    private double z;
    private double m;
    
    public Pointsss(){
        this.x=0.0;
        this.y=0.0;
        this.z=0.0;
        this.m=0.0;
    }

    public Pointsss(double x, double y, double z, double m) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.m = m;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getZ() {
        return z;
    }

    public void setZ(double z) {
        this.z = z;
    }

    public double getM() {
        return m;
    }

    public void setM(double m) {
        this.m = m;
    }
    
    
    
}
